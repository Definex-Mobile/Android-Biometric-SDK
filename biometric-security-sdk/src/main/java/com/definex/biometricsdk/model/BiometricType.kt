package com.definex.biometricsdk.model

/**
 * Represents the type of biometric authentication supported by the device.
 */
enum class BiometricType {
    /**
     * Fingerprint biometric authentication
     */
    FINGERPRINT,
    
    /**
     * Face biometric authentication
     */
    FACE,
    
    /**
     * Iris biometric authentication
     */
    IRIS
}

